/* Sort a list of input words. */

#include "../../std_lib_facilities.h"

int main()
{
	vector<string> words;
	string temp;
	while (cin >> temp) {
		words.push_back(temp);
	}
	cout << "Number of words: " << words.size() << endl;

	sort(words.begin(), words.end());

	cout << "Sorted words:" << endl;
	for (int i = 0; i < words.size(); ++i)
		cout << words[i] << endl;

	return 0;
}
